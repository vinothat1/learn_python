#using for loop
friends=["Palani","raj","selvi"]#Defining a list
for name in friends:#starting a definite loop, defining the list as name as it is the iteration variable for the for loop, Run the statements in the body of the for loop once for each entry in the set named friends
    print("Happy new year:",name,"!!!")
print("Done!")
#we call while statement as indefinite loop because it simply loops until some condition becomes false
#we call for statement a definite loop as it is looping through a known set of items and it runs through as many iterations as there are items in the set.
